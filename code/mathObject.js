/**
 * Math.floor - rounds down to nearest integer
 * Math.round - rounds to nearest integer
 * Math.ceil  - rounds up to nearest integer
 */

console.log(
    Math.floor(0.9),
    Math.floor(1.1),
    Math.round(0.49),
    Math.round(0.5),
    Math.round(2.9),
    Math.ceil(0.1),
    Math.ceil(0.9),
    Math.ceil(21),
    Math.ceil(21.01)
)
